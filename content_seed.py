import json
from pathlib import Path

BASE = Path(__file__).parent
CONTENT_DIR = BASE / "content"
IMAGES_DIR = CONTENT_DIR / "images"
CONTENT_DIR.mkdir(exist_ok=True)
IMAGES_DIR.mkdir(parents=True, exist_ok=True)

def write(day, title, theory, task, examples, quiz_q, options, correct, images):
    obj = {
        "title": title,
        "theory": theory,
        "task": task,
        "images": images,          # локальные пути (загрузим отдельно)
        "examples": examples,
        "quiz": {
            "question": quiz_q,
            "options": options,
            "correct": correct
        }
    }
    fp = CONTENT_DIR / f"day{day:02d}.json"
    with open(fp, "w", encoding="utf-8") as f:
        json.dump(obj, f, ensure_ascii=False, indent=2)

